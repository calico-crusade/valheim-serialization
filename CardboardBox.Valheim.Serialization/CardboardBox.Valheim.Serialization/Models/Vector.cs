namespace CardboardBox.Valheim.Serialization
{
	/// <summary>
	/// Represents a 3D point in the world
	/// </summary>
	public class Vector
	{
		/// <summary>
		/// The X position of the vector (Width)
		/// </summary>
		public float X { get; set; }

		/// <summary>
		/// The Y position of the vector (Height)
		/// </summary>
		public float Y { get; set; }

		/// <summary>
		/// The Z position of the vector (Depth)
		/// </summary>
		public float Z { get; set; }
	}
}
